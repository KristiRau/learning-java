package JavaTasks;

import java.util.Scanner;

public class Task6 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a = scanner.nextInt();
        int b = scanner.nextInt();

        if (b <= a) {
            System.out.println("Job completed");
        } else {
            int sum = 0;
            for (int i = a; a <= b; i++) {
                sum += 1;
            }
        }






    }
}
